using namespace std;

void counter(int n, char* arr, int* counts){
	char alp[26] = {'a','b','c','d','e','f','g','h','i','j','k','l','m','n','o','p','q','r','s','t','u','v','w','x','y','z'};	
	//chars into ints
	for (int i=0; i<n; ++i) int(arr[i]);

	for (int i = 65; i <= 90; ++i){
		for (int j = 0; j < n; ++j){
			if(arr[j] == i){//check if the char array value is equal to the step value of j and convert int to char to check
			 counts[j]+=1;
			}
		}
	}
	//display count
	for (int i = 0; i < 26; ++i) cout << alp[i] << ":"<< counts[i] << endl << endl;		
				
}